
public class Unary {
	
	public static void main(String[] args) {
		
		int n = 10;
		//~은 1의 보수 - 부호는 반대가 되고 절대값은 1이 증가하거나 감소
		System.out.println("~n : " + ~n);
		
		//!(not) - boolean데이터 반전
		boolean b = true;
		System.out.println("!b : " + !b);
		
		n=10;
		//n의 값을 1증가시킵니다.
		//--는 1감소시킵니다.
		n++;
		System.out.println(n);
		
		
	}

}
